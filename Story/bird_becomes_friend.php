        အရောအဝင် အရိုသေတန်မဖြစ်ပေ။သူတို့သည် တစ်ဦးကိုတစ်ဦး လောကဝတ်ကျေပွန်မှုနှင့်
        လေးစားမှုတို့ကိုဆက်လက်ပြကြသည်။အခြားငှက်တို့က ရစ်နှင့်ကျီး ပေါင်းသင်မှုကို
        စိတ်ဝင်စားခြင်းနှင့်စောင့်ကြည့်ကြသည်။သူတို့သည် ငှက်နှစ်ကောင် ရန်ဖြစ်ခြင်း၊ စကားများခြင်းမရှိဘဲ ဤမျှ
        ကာလရှည်ကြာစွာ အတူနေကြသည်ကို အံ့အားသင့်ကြသည်။ နောက်ဆုံး၌ အချို့သောငှက်တို့က သူတို့၏မိတ်ဆွေဖြစ်ခြင်းကို
        ဆန်းစစ်ကြည့်ရန် ဆုံးဖြတ်ကြသည်။ထို့ကြောင့်သူတို့သည် ကျီးအဝေးရောက်ခိုက် ရစ်ထံသို့ သွား၍ 'ရစ်၊ ခင်ဗျား
        ဘာကြောင့် ဘာမျှမရှိတဲ့ ကျီးနဲ့ အတူ နေတာလဲ'၊ 'ခင်ဗျားတို့ ဒီလို မဆိုရဘူး' ဟု ရစ်ကပြန်ပြောသည်။'ကျီးဟာ
        ကျွန်တော့်ထက်ပိုကောင်းတဲ့ ငှက်တစ်ကောင်ဖြစ်တယ်။သူ ကျွန်တော်နှင့်အတူ သစ်ပင်ထဲမှာ နေခြင်းဖြင့်
        ကျွန်တော့်ကို ဂုဏ်ပြုပါတယ်'။နောက်တစ်နေ့တွင် သူတို့သည် ရစ် အဝေးရောက်ခိုက် ကျီးထံသို့ သွား၍ 'ကျီး ခင်ဗျား
        ဘာကြောင့် ဘာမျှအသုံး မကျတဲ့ ရစ်နှင့်အတူ နေတာလဲ' ဟုပြောကြားသည်။ 'ခင်ဗျားတို့ ဒီလိုမဆိုရဘူး'
        ကျီးကပြန်ပြောသည်။'ရစ် ဟာ ကျွန်တော့်ထက်ပိုကောင်းတဲ့ ငှက်တစ်ကောင်ဖြစ်တယ်။သူ ကျွန်တော်နှင့်အတူ သစ်ပင်ထဲမှာ
        နေခြင်းဖြင့် ကျွန်တော့်ကို ဂုဏ်ပြုပါတယ်'။ ငှက်တို့သည် ရစ်နှင့်ကျီး တို့ တစ်ဦးပေါ် တစ်ဦး သဘောထားကို
        လေးလေးနက်နက် စွဲမှတ်သွားကြသည်။(ထို့သို့ သဘောပေါက်သောကြောင့်)သူတို့က သူတို့ကိုယ်သူတို့
        ပြောကြသည်။'ငါတို့လည်း ရစ်နှင့်ကျီး လို ရန်မဖြစ်ဘဲ ၊ စကားမများကြဘဲ ဘာဖြစ်လို့ မနေနိုင်ကြရမှာလဲ'။ဟု
