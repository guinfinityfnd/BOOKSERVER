    ပထမမှာ ငှက်တွေကြားတွင်မိတ်ဆွေဖြစ်ခြင်းကိုမသိကြချေ။အကြောင်းမှာသူတို့အားလုံးကြားတွင်
    ပြင်းထန်သောပြိုင်ဆိုင်မှုရှိနေသည်။ငှက်တစ်ကောင်က အခြားငှက်တစ်ကောင်ကိုမြင်လျှင်
    သူကတစ်ချက်တည်းပြောလိုက်သည်၊'ငါဟာ မင်းထက်သာတဲ့ငှက်တစ်ကောင်ဖြစ်တယ်' ၊အခြားငှက်ကပြန်ပြောသည် 'မဟုတ်တာ
    သေချာတယ်၊ငါကမင်းထက်သာတဲ့ငှက်တစ်ကောင်ဖြစ်တယ်' ၊ထို့နောက်သူတို့ရန်ဖြစ်ရန် စတင်ကြလိမ့်မည်။တစ်နေ့မှာ
    ရစ်သည်ကျီးတစ်ကောင်ကိုတွေ့၍ရန်ဖြစ်ချင်သည့် စိတ်မရှိသည့်အလျောက်ပြော၏ 'ကျီး၊ ခင်ဗျားဟာ
    ကျွန်တော့်ထက်ပိုကောင်းတဲ့ ငှက်တစ်ကောင်ဖြစ်ပါတယ်။' ကျီးကအံ့အား သင့်ရုံမကရစ်၏စကားလုံးများကိုနှစ်သက်သဘောကျ၍
    ယဉ်ကျေးမှု အရပြန်ပြောသည်။ 'မဟုတ်ဘူး ၊ မဟုတ်ဘူး၊ ရစ် ခင်ဗျားက ကျွန်တော့်ထက်ပိုပြီးကောင်းတဲ့
    ငှက်တစ်ကောင်ဖြစ်ပါတယ်။' ငှက်နှစ်ကောင်သည် ထိုင်ပြီးလျှင် စကားစမြည် ပြောကြသည်။ 'ကောင်းပြီ ရစ်' ကျီးက
    ပြန်ပြောသည်။ထို့ကြောင့်သူတို့နှစ်ယောက်သားသည် သစ်ပင်ကြီးတစ်ပင်တည်းတွင် အတူနေကြသည်။အချိန်ကြာမြင့်သော်<span
