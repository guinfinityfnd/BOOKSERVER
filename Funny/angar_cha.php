<p class="text-break">လယ်သမားတစ်ဦး မြေကွက်တစ်ကွက်ကို အငှားယူကာ ဂျုံစိုက်သည်။နံနက်ခင်းမှ ညနေပိုင်းအထိ
    လယ်ကွင်းတွင်သာအချိန်ကုန်သည်။ဇနီးဖြစ်သူကိုလုံးဝ မေ့သွားသည်။
    တစ်ညတွင်အိပ်ရာမှ လန့်နိုးသည်။ သူ၏လက်တစ်ဖက်သည် ဇနီးသည်၏ အနူးညံ့ဆုံးသောနေရာကို
    သွားထိသည်။ဇနီးသည်ကပြောသည်-'ကွင်းထဲမှာချည်း ထွန်မနေနဲ့၊
    အိမ်မှာလည်း ထွန်ဦးရှင့် နို့မဟုတ်ရင် ကျွန်မ အငှားချမိလိမ့်မယ်'.</p>