     မိမိချစ်သူကိုတော့
     ပိုးပန်းရာတွင်တတ်အပ်သည့်ပညာကိုတော့
     ကြားဖူးနားဝဆိုသလို သိထားသည်။ထို့ကြောင့်ရည်းစားစာဖြင့်ပိုးရန်အကြံဖြစ်လေသည်။ထို့ကြေင့်သူသိသမျှ
     ကိုချရေးလိုက်သည်မှာ-'ကိုယ့်ကိုချစ်ရင်ချစ်ပါ ကိုယ့်ကိုမချစ်ရင်
     ရွာကိုမီးရှို့ပစ်မယ်။'ဟု ဖြစ်သည်။</p>